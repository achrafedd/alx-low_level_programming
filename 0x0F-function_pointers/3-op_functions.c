/**
 * op_add - operation add
 *
 * @a: first number
 * @b: second number
 *
 * Return: a + b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - operation sub
 *
 * @a: first number
 * @b: second number
 *
 * Return: a - b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - operation mul
 *
 * @a: first number
 * @b: second number
 *
 * Return: a * b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - operation div
 *
 * @a: first number
 * @b: second number
 *
 * Return: a / b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - operation mod
 *
 * @a: first number
 * @b: second number
 *
 * Return: a % b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
